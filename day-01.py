# Integers: Numbers without decimal points
a = 10
b = 20

# Floats: Numbers with decimal points
f1 = 1.35
pi = 3.14

# Strings: Collection of characters enclosed within a pair of quotes
word = "Hello"
sentence = "Hello World"

# Boolean: a True / False value
adult = True
male = False

# List: A collection of elements with contiguous memory allocation enclosed within a pair of square brackets.
list1 = [1,2,3,4,5]

